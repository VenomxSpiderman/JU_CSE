.MODEL SMALL
.STACK 100H

.DATA
MSG1 DB "ENTER A CHARACTER:$"
MSG2 DB 0DH,0AH,"LOWERCASE CHARACTER:$"

.CODE
MAIN PROC
    MOV AX, @DATA
    MOV DS, AX

    LEA DX, MSG1
    MOV AH, 09H
    INT 21H

    MOV AH, 01H
    INT 21H

    CMP AL, 'A'
    JB DISP
    CMP AL, 'Z'
    JA DISP
    ADD AL, 32

DISP:
    LEA DX, MSG2
    MOV AH, 09H
    INT 21H

    MOV DL, AL
    MOV AH, 02H
    INT 21H

    MOV AH, 4CH
    INT 21H
MAIN ENDP
END MAIN

